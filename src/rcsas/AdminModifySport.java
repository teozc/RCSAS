    private void updateDetails(){
         try{
            BufferedReader br = new BufferedReader(new FileReader("Sport Records.txt"));
            Object[] lines = br.lines().toArray();
            String[] data;
            for (int i = 0; i < lines.length; i++){
                data = lines[i].toString().split("\t");
                if (data[0].equals(SportIDComboBox.getSelectedItem().toString())){
                    SportNameComboBox.setSelectedItem(data[1]);
                    if (data[2].equals("SC001")){
                        KualaLumpurRadio.setSelected(true);
                    }
                    else if (data[2].equals("SC002")){
                        SelangorRadio.setSelected(true);
                    }
                    else if (data[2].equals("SC003")){
                        SabahRadio.setSelected(true);
                    }
                    else if (data[2].equals("SC004")){
                        KedahRadio.setSelected(true);
                    }
                    else if (data[2].equals("SC005")){
                        SarawakRadio.setSelected(true);
                    }
             
                }
            }
            br.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error opening file!", "Error Message", JOptionPane.ERROR_MESSAGE);
        }
        if (SportIDComboBox.getSelectedItem() != null){
            SportNameComboBox.setEnabled(true);
            KualaLumpurRadio.setEnabled(true);
            SelangorRadio.setEnabled(true);
            SabahRadio.setEnabled(true);
            SarawakRadio.setEnabled(true);
            KedahRadio.setEnabled(true);
            UpdateButton.setEnabled(true);
        }       
    }
    
