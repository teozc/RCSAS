    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        try{ 
            BufferedReader br = new BufferedReader(new FileReader("Sport Records.txt"));
            Object[] lines = br.lines().toArray();
            String[] data = null;
            for (int i = 0; i < lines.length; i++){
                data = lines[i].toString().split("\t");
                if (!data[0].equals(SportIDComboBox.getSelectedItem()) && data[1].equals(SportNameComboBox.getSelectedItem()) && data[2].equals(buttonGroup1.getSelection().getActionCommand())){
                    JOptionPane.showMessageDialog(this, "Unable to modify record. Record duplicate exists.", "Error Message", JOptionPane.ERROR_MESSAGE);
                    br.close();
                    return;
                }
            }
            for (int i = 0; i < lines.length; i++){
                data = lines[i].toString().split("\t");
                if (data[0].equals(SportIDComboBox.getSelectedItem())){
                    lines[i] = SportIDComboBox.getSelectedItem() + "\t" + SportNameComboBox.getSelectedItem() + "\t" + buttonGroup1.getSelection().getActionCommand();
                    break;
                }
            }
            br.close();
            BufferedWriter bw = new BufferedWriter(new FileWriter("Sport Records.txt"));
            for (int i = 0; i < lines.length; i++){
                bw.write(lines[i].toString());
                bw.newLine();
            }
            bw.close();
            BufferedReader cbr = new BufferedReader(new FileReader("Coach Records.txt"));
            lines = cbr.lines().toArray();
            for (int i = 0; i < lines.length; i++){
                data = lines[i].toString().split("\t");
                if (data[9].equals(SportIDComboBox.getSelectedItem())){
                    if (!data[10].equals(SportNameComboBox.getSelectedItem()) || !data[7].equals(buttonGroup1.getSelection().getActionCommand())){
                        lines[i] = data[0] + "\t" + data[1] + "\t" + data[2] + "\t" + data[3] + "\t" + data[4] + "\t" + data[5] + "\t" + data[6] + "\t" + data[7] + "\t" + data[8] + "\t" + "None" + "\t" + data[10] + "\t" + data[11];
                    }
                }
                else if (data[7].equals(buttonGroup1.getSelection().getActionCommand()) && data[10].equals(SportNameComboBox.getSelectedItem())){
                    lines[i] = data[0] + "\t" + data[1] + "\t" + data[2] + "\t" + data[3] + "\t" + data[4] + "\t" + data[5] + "\t" + data[6] + "\t" + data[7] + "\t" + data[8] + "\t" + SportIDComboBox.getSelectedItem() + "\t" + data[10] + "\t" + data[11];
                }
            }
            cbr.close();
            BufferedWriter cbw = new BufferedWriter(new FileWriter("Coach Records.txt"));
            for (int i = 0; i < lines.length; i++){
                cbw.write(lines[i].toString());
                cbw.newLine();
            }
            cbw.close();
            JOptionPane.showMessageDialog(this, "Sport record updated successfully.", "Confirmation Message", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error opening file!", "Error Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

