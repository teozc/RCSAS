    private void RemoveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveButtonActionPerformed
        if (RegisteredSportScheduleList.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select the sport schedule(s) that you would like to remove!", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        List<String> selectedSportScheduleIDs = new ArrayList<String>();
        for (String selection: RegisteredSportScheduleList.getSelectedValuesList()) {
            selectedSportScheduleIDs.add(selection.substring(selection.indexOf('(') + 1, selection.indexOf(')')));
        }
        int result = Student.removeRegisteredSportSchedules(selectedSportScheduleIDs);
        switch(result) {
            case 0:
                JOptionPane.showMessageDialog(this, "Modification process successful!", "Congratualations", JOptionPane.ERROR_MESSAGE);
                break;
            case -1:
                JOptionPane.showMessageDialog(this, "Error changing file content!", "Error Message", JOptionPane.ERROR_MESSAGE);
                break;
        }
        displayRegisteredSportSchedules();
    }//GEN-LAST:event_RemoveButtonActionPerformed

