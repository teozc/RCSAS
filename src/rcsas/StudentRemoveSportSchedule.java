    public void displayRegisteredSportSchedules() {
        model.clear();
        List<String> registeredSportSchedules = Student.getRegisteredSportScheduleList();
        for (String registeredSportSchedule: registeredSportSchedules) {
            model.addElement(registeredSportSchedule);
        }
    }
    
