    private void updateRadioButton(){
        buttonGroup1.clearSelection();
        try {
            BufferedReader br = new BufferedReader(new FileReader("Sport Records.txt"));
            KualaLumpurRadio.setEnabled(true);
            SelangorRadio.setEnabled(true);
            SabahRadio.setEnabled(true);
            KedahRadio.setEnabled(true);
            SarawakRadio.setEnabled(true);
            String temp;
            String[] data;
            String sportName = SportNameComboBox.getSelectedItem().toString();
            while ((temp = br.readLine()) != null) {
                data = temp.split("\t", -1);
                if (data[1].equals(sportName)){
                    try {
                        BufferedReader cbr = new BufferedReader(new FileReader("Sport Centers.txt"));
                        String[] cdata;
                        while ((temp = cbr.readLine()) != null) {
                            cdata = temp.split("\t", -1);
                            if (cdata[0].equals(data[2])) {
                                if (cdata[0].equals("SC001")) {
                                    KualaLumpurRadio.setEnabled(false);
                                    KualaLumpurRadio.setSelected(false);
                                }
                                if (cdata[0].equals("SC002")) {
                                    SelangorRadio.setEnabled(false);
                                    SelangorRadio.setSelected(false);
                                }
                                if (cdata[0].equals("SC003")) {
                                    SabahRadio.setEnabled(false);
                                    SabahRadio.setSelected(false);
                                }
                                if (cdata[0].equals("SC004")) {
                                    KedahRadio.setEnabled(false);
                                    KedahRadio.setSelected(false);
                                }
                                if (cdata[0].equals("SC005")) {
                                    SarawakRadio.setEnabled(false);
                                    SarawakRadio.setSelected(false);
                                }
                            }
                        }
                        cbr.close();
                    } 
                    catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Error opening file!", "Error Message", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }
            }
            br.close();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error opening file!", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }
    
    private String getSportCenterID(String name) {
        try {
            BufferedReader br = new BufferedReader(new FileReader("Sport Centers.txt"));
            String temp;
            String[] data;
            while ((temp = br.readLine()) != null) {
                data = temp.split("\t", -1);
                if (name.equals(data[1])){
                    return data[0];
                }
            }
            br.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error opening file!", "Error Message", JOptionPane.ERROR_MESSAGE);
        }
        return "MISSING";
    }

