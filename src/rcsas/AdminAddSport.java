    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        if (buttonGroup1.getSelection() == null){
            JOptionPane.showMessageDialog(this, "Please choose a sport center.", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else{
            try{
                BufferedReader br = new BufferedReader(new FileReader("Sport Records.txt"));
                int line = 0;
                while (br.readLine() != null){
                    line++;
                }
                line++;
                String sportID = "SP" + String.format("%03d", line);
                String sportName = SportNameComboBox.getSelectedItem().toString();
                String sportCenterID;
                if (KualaLumpurRadio.isSelected()){
                    sportCenterID = getSportCenterID("Kuala Lumpur");
                }
                else if (SelangorRadio.isSelected()){
                    sportCenterID = getSportCenterID("Selangor");
                }
                else if (SabahRadio.isSelected()){
                    sportCenterID = getSportCenterID("Sabah");
                }
                else if (KedahRadio.isSelected()){
                    sportCenterID = getSportCenterID("Kedah");
                }
                else if (SarawakRadio.isSelected()){
                    sportCenterID = getSportCenterID("Sarawak");
                }
                else{
                    sportCenterID = "MISSING";
                }
                br.close();
                BufferedWriter bw = new BufferedWriter(new FileWriter("Sport Records.txt", true));
                bw.write(sportID + "\t" + sportName + "\t" + sportCenterID + "\n");
                bw.close();
                try {
                    BufferedReader cbr = new BufferedReader(new FileReader("Coach Records.txt"));
                    String temp;
                    String[] data;
                    String[] fileContent = cbr.lines().collect(Collectors.joining("\n")).split("\n", -1);
                    for (int i = 0; i < fileContent.length; i++){
                        data = fileContent[i].split("\t", -1);
                        if (sportName.equals(data[10]) && sportCenterID.equals(data[7])) {
                            data[9] = sportID;
                            Array.set(fileContent, i, data[0] + "\t" + data[1] + "\t" + data[2] + "\t" + data[3] + "\t" + data[4] + "\t" + data[5] + "\t" + data[6] + "\t" + data[7] + "\t" + data[8] + "\t" + data[9] + "\t" + data[10] + "\t" + data[11]);                            
                        }
                    }
                    cbr.close();
                    BufferedWriter cbw = new BufferedWriter(new FileWriter("Coach Records.txt"));
                    for (int i = 0; i < fileContent.length; i++){
                        cbw.write(fileContent[i] + "\n");
                    }
                    cbw.close();
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(this, "Error opening file!", "Error Message", JOptionPane.ERROR_MESSAGE);
                }
                JOptionPane.showMessageDialog(this, "Sport record added successfully", "Confirmation Message", JOptionPane.INFORMATION_MESSAGE);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this, "Error opening file!", "Error Message", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
    }//GEN-LAST:event_AddButtonActionPerformed

