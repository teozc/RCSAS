    private void CheckButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckButtonActionPerformed
        try {
            model.clear();
            String selection = SportButtonGroup.getSelection().getActionCommand();
            List<String> SportScheduleList = Student.getSportScheduleList(selection);
            for (String SportSchedule: SportScheduleList) {
                model.addElement(SportSchedule);
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please select one of the sports!", "Error Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_CheckButtonActionPerformed

