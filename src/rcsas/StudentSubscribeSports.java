    private void SubscribeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubscribeButtonActionPerformed
        if (SportScheduleList.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select the sport schedule(s) that you would like to subscribe!", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        List<String> selectedSportScheduleIDs = new ArrayList<String>();
        String selectedSportName = SportButtonGroup.getSelection().getActionCommand();
        List<String> selectedSportSchedules = SportScheduleList.getSelectedValuesList();
        for (String sportSchedule: selectedSportSchedules) {
            selectedSportScheduleIDs.add(sportSchedule.substring(sportSchedule.indexOf("(") + 1, sportSchedule.indexOf(")")));
        }
        int result = Student.modifyRegisteredSportSchedules(selectedSportName, selectedSportScheduleIDs);
        switch(result) {
            case 0:
                JOptionPane.showMessageDialog(this, "Subscription process completed!", "Congratualations", JOptionPane.ERROR_MESSAGE);
                break;
            case -1:
                JOptionPane.showMessageDialog(this, "Error writing into file!", "Error Message", JOptionPane.ERROR_MESSAGE);
                break;
        }
        int sportFee = selectedSportSchedules.size() * 150;
        SportFeeText.setText("RM" + sportFee);
    }//GEN-LAST:event_SubscribeButtonActionPerformed
    
    public void displaySports() {
        List<String> SportNames = Student.getSportList();
        for (String sportName: SportNames) {
            switch(sportName) {
                case "Swimming":
                    SwimmingButton.setEnabled(true);
                    break;
                case "Badminton":
                    BadmintonButton.setEnabled(true);
                    break;
                case "Football":
                    FootballButton.setEnabled(true);
                    break;
                case "Archery":
                    ArcheryButton.setEnabled(true);
                    break;
                case "Gymnastics":
                    GymnasticsButton.setEnabled(true);
                    break;
                case "Volleyball":
                    VolleyballButton.setEnabled(true);
                    break;
                case "Basketball":
                    BasketballButton.setEnabled(true);
                    break;
                case "Cricket":
                    CricketButton.setEnabled(true);
                    break;
                case "Tennis":
                    TennisButton.setEnabled(true);
                    break;
                case "Table Tennis":
                    TableTennisButton.setEnabled(true);
                    break;
            }
        }
    }
    
