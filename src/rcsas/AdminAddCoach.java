    private void MonthBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MonthBoxActionPerformed
        DefaultComboBoxModel model = (DefaultComboBoxModel)DayBox.getModel();
        date.month = (String)MonthBox.getSelectedItem();
        switch (date.getMonthType()){
            case 0:{
                checkAndAddItem(model, "29");
                checkAndAddItem(model, "30");
                checkAndAddItem(model, "31");
                break;
            }
            case 1:{
                checkAndAddItem(model, "29");
                checkAndAddItem(model, "29");
                DayBox.removeItem("31");
                break;
            }
            case 2:{
                DayBox.removeItem("30");
                DayBox.removeItem("31");
                if (!date.isLeapYear()){
                    DayBox.removeItem("29");
                }
            }
        }
    }//GEN-LAST:event_MonthBoxActionPerformed

    private void YearBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_YearBoxActionPerformed
        DefaultComboBoxModel model = (DefaultComboBoxModel)DayBox.getModel();
        date.year = (String)YearBox.getSelectedItem();
        if (date.isLeapYear()){
            if (date.month.equals("2")){
                checkAndAddItem(model, "29");
            }
        }
        else{
            DayBox.removeItem("29");
        }
    }//GEN-LAST:event_YearBoxActionPerformed

