    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        if (NameText.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter a name.", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(HourlyRateText.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter an hourly rate.", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            float HourlyRate = Float.parseFloat(HourlyRateText.getText().trim());
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Please enter a valid hourly rate.", "Error Message", JOptionPane.ERROR_MESSAGE);
        }
        if(PhoneText.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter a phone number.", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(AddressText.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter an address.", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(PostalCodeText.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter a postal code.", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(CityText.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter a city name.", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Coach c = new Coach(NameText.getText(), String.format("%s/%s/%s", date.day, date.month, date.year), "None", Float.parseFloat(HourlyRateText.getText()), PhoneText.getText(), String.format("%s, %s, %s, %s", AddressText.getText(), PostalCodeText.getText(), CityText.getText(), StateBox.getSelectedItem().toString()), SportCenterBox.getSelectedItem().toString().split(" ")[0], "None", SportBox.getSelectedItem().toString(), "None");
        int e = c.addRecord();
        if (e == 0){
            JOptionPane.showMessageDialog(this, "Coach record added successfully.", "Confirmation Message", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(this, "Error opening file!", "Error Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_AddButtonActionPerformed

